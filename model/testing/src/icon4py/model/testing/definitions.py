    match experiment:
        case Experiments.MCH_CH_R04B09:
            lowest_layer_thickness = 20.0
            model_top_height = 23000.0
            stretch_factor = 0.65
            damping_height = 12500.0
            rayleigh_coeff = 5.0
            exner_expol = 0.333
