class ExperimentDefinitionTuple(NamedTuple):
    code: str
    description: str
    grid: Grid
    num_levels: int
    partitioned_data: Mapping[int, str] | None = None


def experiment_definition(
    *,
    code: str,
    description: str,
    grid: Grid,
    num_levels: int,
    partitioned_data: Mapping[int, str] | None,
) -> ExperimentDefinitionTuple:
    return ExperimentDefinitionTuple(
        code=code,
        description=description,
        grid=grid,
        num_levels=num_levels,
        partitioned_data=partitioned_data,
    )


