class GridKind(str, enum.Enum):
    REGIONAL = "REGIONAL"
    GLOBAL = "GLOBAL"
    TORUS = "TORUS"

    def __repr__(self):
        return str.__repr__(self)

    def __str__(self):
        return str.__str__(self)


class GridSizesDict(TypedDict):
    cell: ReadOnly[int]
    edge: ReadOnly[int]
    vertex: ReadOnly[int]


class GridDefinitionTuple(NamedTuple):
    description: str
    kind: GridKind
    sizes: GridSizesDict
    file_name: str | None = None
    uri: str | None = None
    uuid: uuid.UUID | None = None


def grid_definition(
    *,
    description: str,
    kind: GridKind,
    sizes: GridSizesDict,
    file_name: str | None,
    uri: str | None,
    uuid: uuid.UUID | None,
) -> GridDefinitionTuple:
    return GridDefinitionTuple(
        description=description,
        kind=kind,
        sizes=GridSizesDict(**sizes),
        file_name=file_name,
        uri=uri,
        uuid=uuid,
    )


